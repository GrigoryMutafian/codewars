function calcAndMultyByTwo(equals) {
  return equals * 2;
}
function addition(num1, num2) {
  return num1 + num2;
}

function subtract(num1, num2) {
  return num1 - num2;
}

function multiply(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return num1 / num2;
}

console.log(calcAndMultyByTwo(divide(5, 5)));
console.log(calcAndMultyByTwo(addition(5, 5)));
console.log(calcAndMultyByTwo(subtract(5, 5)));
console.log(calcAndMultyByTwo(multiply(5, 5)));
